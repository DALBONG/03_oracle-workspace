FROM TB_RENT R, TB_MEMBER M, TB_BOOK B, TB_PUBLISHER P
WHERE R.RENT_MEM_NO = M.MEMBER_NO
  AND R.RENT_BOOK_NO = B.BK_NO
  AND B.BK_PUB_NO = P.PUB_NO
  AND BK_NO = 2;






















